def build_customer_dashboard_summary(
    settings: Settings,
    *,
    db_session: Session | None,
    monitoring_snapshot: dict[str, Any],
    source_inventory_snapshot: dict[str, Any],
) -> dict[str, Any]:
    live_opportunities: list[dict[str, Any]] = []
    if db_session is not None:
        try:
            live_opportunities = build_live_candidate_opportunities(db_session)
        except Exception:
            live_opportunities = []

    snapshot_opportunities = [] if live_opportunities else _load_client_contender_snapshot(settings)
    if live_opportunities:
        data_mode = "live_candidate_scoring"
        opportunities = live_opportunities
    elif snapshot_opportunities:
        data_mode = "client_snapshot"
        opportunities = snapshot_opportunities
    else:
        data_mode = "seeded_catalog"
        opportunities = _build_opportunities()
    market_backed_mode = data_mode in {"live_candidate_scoring", "client_snapshot"}
    featured_opportunities = opportunities[:6]
    corridors = _build_corridors(opportunities)
    unique_universities = sorted(
        {item["university_anchor"] for item in opportunities},
    )
    generated_at = datetime.now(UTC).isoformat()
    priority_now_count = sum(
        1 for item in opportunities if item["readiness_stage"] == "Priority now"
    )
    top_tier_count = sum(1 for item in opportunities if item["score_band"] == "Tier 1")
    opportunity_count = len(opportunities)

    return {
        "app_name": settings.app_name,
        "display_name": "Texas Private Client Siting Portal",
        "environment": settings.app_env,
        "version": "0.1.0",
        "generated_at": generated_at,
        "market": "Texas",
        "data_mode": data_mode,
        "hero_title": (
            f"{opportunity_count} live-ranked Texas opportunities balanced across major metros."
            if market_backed_mode
            else (
                f"{opportunity_count} ranked Texas opportunities near cities, utilities, "
                "and university talent."
            )
        ),
        "hero_subtitle": (
            "Candidate-first grading is using live marketed sites plus public infrastructure "
            "signals, with a social-political approval overlay and minimum major-metro coverage "
            "for client review."
            if market_backed_mode
            else (
                "Private shortlist designed to help client principals and site selectors focus on "
                "metros and corridors where power, fiber, land scale, workforce depth, and "
                "approval friction align."
            )
        ),
        "opportunity_count": opportunity_count,
        "priority_now_count": priority_now_count,
        "top_tier_count": top_tier_count,
        "corridor_count": len(corridors),
        "university_count": len(unique_universities),
        "metrics": _build_metrics(
            opportunities,
            priority_now_count=priority_now_count,
            corridor_count=len(corridors),
            university_count=len(unique_universities),
            source_inventory_snapshot=source_inventory_snapshot,
            data_mode=data_mode,
        ),
        "filters": _build_filters(opportunities, unique_universities=unique_universities),
        "featured_opportunities": featured_opportunities,
        "opportunities": opportunities,
        "corridors": corridors,
        "data_coverage": source_inventory_snapshot,
        "monitoring": monitoring_snapshot,
    }


def _load_client_contender_snapshot(settings: Settings) -> list[dict[str, Any]]:
    path = Path(settings.client_contender_snapshot_path)
    if not path.is_absolute():
        path = Path.cwd() / path

    try:
        payload = json.loads(path.read_text(encoding="utf-8"))
    except (OSError, json.JSONDecodeError):
        return []

    opportunities = payload.get("opportunities")
    if not isinstance(opportunities, list):
        return []

    return [dict(item) for item in opportunities if isinstance(item, dict)]


def _build_opportunities() -> list[dict[str, Any]]:
    ranked = sorted(
        _OPPORTUNITY_SEEDS,
        key=lambda item: (
            -item.viability_score,
            -item.power_score,
            -item.fiber_score,
            item.site_name,
        ),
    )

    opportunities: list[dict[str, Any]] = []
    for rank, seed in enumerate(ranked, start=1):
        approval_overlay = build_social_political_overlay(
            metro_name=seed.metro,
            region=seed.region,
            city_distance=float(seed.distance_to_city_miles),
            university_distance=float(seed.distance_to_university_miles),
            acreage=_seed_acreage_estimate(seed.acreage_band),
            building_sqft=None,
            water_score=seed.water_score,
            environment_score=max(seed.water_score - 4, 52),
            hazard_score=max(seed.water_score - 6, 50),
        )
        acreage_estimate = _seed_acreage_estimate(seed.acreage_band)
        opportunities.append(
            {
                "rank": rank,
                "site_id": _site_id(seed, rank),
                "site_name": seed.site_name,
                "corridor_name": seed.corridor_name,
                "metro": seed.metro,
                "city": seed.city,
                "county": seed.county,
                "region": seed.region,
                "university_anchor": seed.university_anchor,
                "acreage": acreage_estimate,
                "acreage_band": seed.acreage_band,
                "distance_to_city_miles": seed.distance_to_city_miles,
                "distance_to_university_miles": seed.distance_to_university_miles,
                "viability_score": seed.viability_score,
                "power_score": seed.power_score,
                "fiber_score": seed.fiber_score,
                "water_score": seed.water_score,
                "talent_score": seed.talent_score,
                "readiness_stage": seed.readiness_stage,
                "score_band": _score_band(seed.viability_score),
                "strengths": [
                    seed.power_profile,
                    seed.fiber_profile,
                    seed.water_profile,
                ],
                "summary": (
                    f"Strong on {seed.power_profile}, {seed.fiber_profile}, and "
                    f"{seed.water_profile} with access to {seed.university_anchor} talent."
                ),
                "lat": seed.lat,
                "lon": seed.lon,
                **approval_overlay,
            }
        )
    return opportunities


def _build_corridors(opportunities: list[dict[str, Any]]) -> list[dict[str, Any]]:
    grouped: dict[str, list[dict[str, Any]]] = {}
    for item in opportunities:
        grouped.setdefault(item["corridor_name"], []).append(item)

    corridors: list[dict[str, Any]] = []
    for corridor_name, items in grouped.items():
        corridors.append(
            {
                "name": corridor_name,
                "site_count": len(items),
                "average_viability_score": round(
                    mean(item["viability_score"] for item in items),
                    1,
                ),
                "priority_now_count": sum(
                    1 for item in items if item["readiness_stage"] == "Priority now"
                ),
                "lead_metro": items[0]["metro"],
                "lead_university": items[0]["university_anchor"],
            }
        )

    return sorted(
        corridors,
        key=lambda item: (-item["average_viability_score"], -item["site_count"], item["name"]),
    )


