def _build_filters(
    opportunities: list[dict[str, Any]],
    *,
    unique_universities: list[str],
) -> dict[str, list[str]]:
    return {
        "metros": sorted({item["metro"] for item in opportunities}),
        "readiness_stages": sorted({item["readiness_stage"] for item in opportunities}),
        "corridors": sorted({item["corridor_name"] for item in opportunities}),
        "university_anchors": unique_universities,
    }


def _seed_acreage_estimate(acreage_band: str) -> float | None:
    matches = re.findall(r"\d+(?:\.\d+)?", acreage_band)
    if not matches:
        return None
    values = [float(item) for item in matches[:2]]
    if len(values) == 1:
        return values[0]
    return round(mean(values), 1)


def _score_band(score: int) -> str:
    if score >= 92:
        return "Tier 1"
    if score >= 86:
        return "Tier 2"
    return "Tier 3"


def _site_id(seed: OpportunitySeed, rank: int) -> str:
    return f"tx-{_slugify(seed.corridor_name)}-{_slugify(seed.city)}-{rank:02d}"


def _slugify(value: str) -> str:
    return "-".join(
        chunk
        for chunk in "".join(
            character.lower() if character.isalnum() else " "
            for character in value
        ).split()
        if chunk
    )
